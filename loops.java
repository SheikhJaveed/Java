public class loops {
    public static void main(String[] args) {
        //for loop
        for(int i=0;i<11;i++){
            System.out.print(i+" "); //prints in same line
        }
        //while loop
        int i=0;
        while(i<11){
            System.out.print(i+" ");
            i++;
        }
        //do while loop
        int j=0;
        do{
            System.out.print(j+" ");
            j++;
        }while(j<11);
    }
}
